class Calculator:

    def __init__(self):
        pass

    def add(self, a, b):
         return a + b

# This function subtracts two numbers
    def subtract(self, a, b):
        return a - b

# This function multiplies two numbers
    def multiply(self, a, b):
        return a * b

# This function divides two numbers
    def divide(self, a, b):
        return a / b


